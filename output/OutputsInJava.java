package output;

public class OutputsInJava {
    public static void main(String[] ars){
//               Output in single line without break

        System.out.print("Hello World with Java");
        System.out.print("Hello World with Java");
        System.out.println("\n");
        //        Output in NextLine

        System.out.println("Hello World with Java");
        System.out.println("Hello World with Java");
        System.out.println("Hello World with Java");
        System.out.println("");


//                 Output in NextLine using "\n"
        System.out.print("Hello world with Java\n");
        System.out.print("Hello world with Java\n");
        System.out.print("Hello world with Java\n");
        System.out.println("Hello World with Java\nfrom Bangladesh");
        System.out.println("Hello World with Java\nfrom\n 'Bangladesh'\n");

//        ProblemSolving_Print the *
        System.out.println("*");
        System.out.println("**");
        System.out.println("***");
        System.out.println("****");

    }
}
